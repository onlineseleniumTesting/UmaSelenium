package Package1;

public class Edemo {

	private int eno;
	private String empName;
	private int empAge;
	
	public void seteno(int newValue) {
		eno = newValue;
		
	}
	public void setempName(String newValue) {
		empName = newValue;
		
	}
	public void setempAge(int newValue) {
		empAge = newValue;
		
	}
	
	public int geteno() {
		return eno;
	}
	
	public String getempName() {
		return empName;
	}
	
	public int getempAge() {
		return empAge;
	}
}
